package com.contarbn.model.reports;

public class NotaResoDataSource {

    private String numero;

    private String data;

    private String fornitorePartitaIva;

    private String fornitoreCodiceFiscale;

    private String fornitoreDescrizione;

    private String causale;

    private String pagamento;

    private String agente;

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getFornitorePartitaIva() {
        return fornitorePartitaIva;
    }

    public void setFornitorePartitaIva(String fornitorePartitaIva) {
        this.fornitorePartitaIva = fornitorePartitaIva;
    }

    public String getFornitoreCodiceFiscale() {
        return fornitoreCodiceFiscale;
    }

    public void setFornitoreCodiceFiscale(String fornitoreCodiceFiscale) {
        this.fornitoreCodiceFiscale = fornitoreCodiceFiscale;
    }

    public String getFornitoreDescrizione() {
        return fornitoreDescrizione;
    }

    public void setFornitoreDescrizione(String fornitoreDescrizione) {
        this.fornitoreDescrizione = fornitoreDescrizione;
    }

    public String getCausale() {
        return causale;
    }

    public void setCausale(String causale) {
        this.causale = causale;
    }

    public String getPagamento() {
        return pagamento;
    }

    public void setPagamento(String pagamento) {
        this.pagamento = pagamento;
    }

    public String getAgente() {
        return agente;
    }

    public void setAgente(String agente) {
        this.agente = agente;
    }

}
